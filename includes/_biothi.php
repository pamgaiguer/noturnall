<p style="font-size: 20px">BIOGRAFIA</p><br>						
<p>Filho da cantora Maria Odette, que lançou Caetano Veloso, ganhadora de vários festivais da MPB entre as décadas de 60 e 90.</p>
<br>
<p>Vocalista das bandas NOTURNALL, SHAMAN, KARMA, ARENA e VOX.</p>
<br>
<p>Produtor de centenas de discos, dentre eles os de bandas e artistas bastantes conhecidos no cenário do rock brasileiro e mundial, como NOTURNALL, SHAMAN, KARMA, ANGRA, KIKO LOUREIRO, EDU ARDANUY, TARJA TURUNEN, TIMO TOLKKI, TUATHA DE DANNAN, CÓLERA, REPÚBLICA, WIZARDS, KAVLA. </p>
<br>
<p>Proprietário do FUSÃO VM&T STUDIOS, que já está na sua 7a sede e é um dos estúdios “de referência” do mercado de áudio e vídeo no Brasil e no mundo. Duas vezes capa da revista mais conceituada de estúdios no Brasil, a ‘Áudio, Música & Tecnologia’.</p>
<br>
<p>Thiago também já ganhou três discos de Ouro, dois deles fora do Brasil.</p>
<br>
<p>Assinou duas vezes a Calçada da Fama do Rock Brasileiro, uma pelo SHAMAN e outra pela NOTURNALL.</p>
<br>
<p>Hoje conta com patrocinadores de peso: RØDE MICROPHONES, EVENT MONITORS, ASK RACKS, 
LYCO HEADPHONES, HK ÁUDIO SPEAKERS, STEINBERG/YAMAHA, SANTO ANGELO CABLES, ADIDAS CLOTHES, UMBRO CLOTHES, EMT MUSIC SCHOOL, EAGLE INSTRUMENTS e FOGGY FILMS.</p>
<br>
<p>Representado pelas agências BASE 2 (MONIKKA CAVALERA) do Brasil, SOL MUSIC (TIZIANA HURD) no Canadá & América Latina, KILLSHOT (ANTHONY POLLOCK) na Austrália, e METALVILLE (HOLGER KOSH) na Europa e Estados Unidos.</p>
<br>
<p>Thiago Bianchi é o único vocalista brasileiro de Rock/Heavy a ter um DVD inteiramente gravado na EUROPA, acompanhado pela maior Orquestra de Praga, a BOUSLAV ORCHESTRA, num dos maiores festivais de Metal do MUNDO, o MASTERS of ROCK.</p>
<br>
<p>Ele é também o único cantor brasileiro a estourar uma taça de cristal, usando apenas a voz, diante das câmeras de TV do FANTÁSTICO, da REDE GLOBO.</p>
<br>
<p>Sobrevivente de um câncer no mediastino, que na época foi considerado inoperável e o lhes deram somente mais seis meses de vida, hoje ele está completamente curado e saudável.</p>
<br>
<p>Thiago já possui mais de 10 video-clipes no seu currículo, tanto como roteirista, produtor e diretor.</p>
<br>
<p>Vocalista da única banda nacional a ter um clipe gravado na cidade de Nova Iorque, com a participação de um dos maiores ícones do Heavy Metal mundial, Sir RUSSELL ALLEN, vocalista das bandas Symphony X e Adrenaline Mob.</p>
<br>
<p>Thiago Bianchi é considerado atualmente uma das maiores vozes do cenário Heavy Metal brasileiro e mundial.</p>
<br>
<p>Além do seu trabalho árduo na produção de grandes álbuns de grandes bandas no seu estúdio, ele atualmente está terminando o trabalho para o lançamento do debut da sua nova super-banda, NOTURNALL, e dirigindo o segundo video-clipe da mesma.</p>		
