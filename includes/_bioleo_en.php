<p style="font-size: 20px">BIOGRAPHY</p><br>			
<p>﻿With a career of more than 20 years, Leo Mancini already played his music in many stages in Latin America, North America and Europe. Guitar player of the bands Shaman and Tempestt, very well known in Brazil and abroad, he has already released five (5) studio albums and two (2) live DVDs in Brazil, Europe and Japan.</p>
<br><br>

<p>He is a member of the new band, Noturnall, which is releasing its debut album in February 2104. It's a project with members of the band Shaman joining forces with drummer Aquiles Priester, produced by Russell Allen, lead singer of the renowned bands Symphony X and Adrenaline Mob.</p>
<br><br>
<p>He teaches guitar since the age of 15, he has private students and also teaches classes on music schools and conservatories, including the Conservatório Musical Souza Lima, in Sao Paulo.</p>
<br><br>
<p>He works as a music producer and sound engineer since 1997, producing soundtracks, jingles, new bands and artists.</p>
<br><br>
<p>He also produced his solo album, "Leo Mancini - Acoustic Hits", thus establishing his career and himself as musician, composer, arranger, sound engineer and singer. It was released by Som Livre and in 2011 he had a single on a tv show called Malhação, from the huge Brazilian TV channel Globo.</p>
<br><br>
<p>Throughout his career he supported numerous international artists on their live shows and recordings, like Jeff Scott Soto (Talisman, Journey), Tim "Ripper" Owens (Judas Priest), Steve Augeri (Journey), Billy Sheehan (Mr.Big, Winery Dogs), etc.</p>
<br><br>
<p>He is currently finishing his second solo project, called "Acoustic Hits II", which is a partnership with Alpha FM radio.</p>

