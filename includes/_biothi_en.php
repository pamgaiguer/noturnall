<p style="font-size: 20px">BIOGRAPHY</p><br>	

<p>Son of singer Maria Odette , who launched Caetano Veloso, and winner of many MPB festivals between the 60s and 90s.</p>
<br>
<p>Singer of the new super-band NOTURNALL, and the already successful and established bands SHAMAN, KARMA, ARENA and VOX.</p><br>
<p>Producer of hundreds of albums from bands and artists known and successful worldwide like NOTURNALL, SHAMAN, KARMA, ANGRA, KIKO LOUREIRO, EDU ARDANUY, TARJA TURUNEN, TIMO TOLKKI, TUATHA DE DANNAN, CÓLERA, REPÚBLICA, WIZARDS, KAVLA.</p><br>
<p>Owner of FUSÃO VM&T STUDIOS, which is now in its seventh address, and is one of the ‘reference studios’ for the audio and video markets in Brazil and worldwide. Twice in the cover of one of the most prestigious studio magazines in Brazil, 'Audio , Music & Technology'.</p><br>
<p>Thiago has also won three Gold records, two of them out of Brazil.</p><br>
<p>He already left his hand mark twice on the Brazilian Rock Walk of Fame, with SHAMAN and NOTURNALL.</p><br>
<p>Today he’s partner and proudly sponsored by RØDE MICROPHONES, EVENT MONITORS, ASK RACKS, LYCO HEADPHONES, HK ÁUDIO SPEAKERS, STEINBERG/YAMAHA, SANTO ANGELO CABLES, ADIDAS CLOTHES, UMBRO CLOTHES, EMT MUSIC SCHOOL, EAGLE INSTRUMENTS and FOGGY FILMS.</p><br>
<p>Represented by big agencies all over the world: BASE 2 (MONIKKA CAVALERA) in Brazil, SUN MUSIC (TIZIANA HURD) in Canada & Latin America, KILLSHOT (ANTHONY POLLOCK) in Australia, and METALVILLE (HOLDEN KOSH) in Europe and the United States.</p><br>
<p>Thiago Bianchi is the only Brazilian vocalist of rock/heavy to have a DVD fully recorded in Europe, accompanied by the greater Orchestra of Prague, BOUSLAV ORCHESTRA, in one of the biggest Metal Festivals of the world, the MASTERS Of ROCK.</p><br>
<p>He is also the only Brazilian singer able to break a crystal glass cup using only his voice, right in front of the TV cameras of Fantastico, a famous TV show in Brazil, by Rede Globo.</p><br>
<p>Survivor of a rare type of mediastinum cancer, which at the time was considered inoperable, and he was given only six months to live. Today he is completely healed and healthy.</p><br>
<p>Thiago has already more than 10 video clips on his resume, as writer, producer and director.</p><br>
<p>Singer of the only Brazilian band to have a music video recorded in New York City, with the participation of one of the greatest icons of the Heavy Metal world, Sir RUSSELL ALLEN, lead singer of the bands Symphony X and Adrenaline Mob.</p><br>
<p>Thiago Bianchi is currently considered one of the greatest Brazilian voices of the Heavy Metal worldwide.</p><br>
<p>Besides his hard work in producing great albums from great bands in his studio, he is currently finishing working on the debut album of the new super-band NOTURNALL, as well as directing the second music video of this new release.</p><br>

							
