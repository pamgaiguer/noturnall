<p style="font-size: 20px">BIOGRAFIA</p><br>			
<p>Com mais de 20 anos de carreira, Leo Mancini, levou sua música aos palcos da América Latina, América do Norte e Europa. <br>
Guitarrista das bandas Shaman e Tempestt, conhecidas no Brasil e exterior, possui 5(cinco) trabalhos em estúdio e 2(dois) Dvds lançados no Brasil, Europa e Japão.  Recentemente, lançou o projeto Noturnall com integrantes da banda Shaman e o baterista Aquiles Priester e foi produzido por Russell Allen, vocalista da renomada banda Symphony X .</p>
<br>
<p>Leciona guitarra desde os 15 anos de idade e foi professor em escolas e conservatórios musicas, dentre eles, o Conservatório Musical Souza Lima em São Paulo.</p>
<br>
<p>Trabalha como produtor musical e engenheiro de som desde 1997 produzindo trilhas, jingles, artistas e bandas.</p>
<br>
<p>Produziu seu projeto solo, “Leo Mancini – Acoustic Hits” e, com isso, obteve grande reconhecimento como músico, arranjador, engenheiro de som e vocalista. Seu trabalho foi lançado pela Som Livre e foi trilha da série Malhação 2011.</p>
<br>
<p>Ao longo de sua carreira acompanhou inúmeros artistas internacionais: Jeff Scott Soto (Talisman, Journey), Tim “Ripper” Owens (Judas Priest), Steve Augeri (Journey), Billy Sheehan (Mr. Big), etc.
<br>
Atualmente esta finalizando o segundo cd do projeto “Acoustic Hits” que tem a parceria da rádio Alpha Fm. </p>		
