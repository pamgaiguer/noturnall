<p style="font-size: 20px">BIOGRAPHY</p><br>	
<p>﻿Fernando Badra Quesada was born on January 24 1986, he is 27 years old.</p>
<br>
<p>He began playing drums and guitar when he was 8. A Self taught artist, soon after learned to play guitar and bass. Had his first band at the age of 11, it was an Iron Maiden tribute band. And then at the age of 12, he started writing his own music, put together a new band and even won some music festivals organized his school at the time, Guilherme Dumont Villares, in São Paulo.</p>
<br>

<p>At 15, Fernando started looking for a job. As he always liked music, studios, and recording sounds, he decided to look for a trainee position on a music recording studio, where he could learn more about it. He made up a list of the 21 biggest and better music studios of Sao Paulo and on a bus he went. Visiting the first 10 addresses, he offered all sorts of work in exchange of learning of the craft. On the 11th name/address, he met with Thiago Bianchi and his Contato Studio and was accepted. It was there and then that Fernando gave its first steps on becoming a great musician and musical producer. He worked cleaning toilets, drum sets, recording rooms, setting up cables and gear, and in exchange Thiago was teaching him all about a music studio routine, musical production and the current professional music/rock scene.</p>
<br>
<p>Fernando found out he had a great potential to write music, and started writing material for the bands that came into the studio to record a album. A jump-start on his wishes and interest to learn more and become an audio professional.</p>
<br>
<p>When Thiago founded another studio, Via Musique, Fernando followed him and started writing material to artist coming from the Warner records. He was also involved on the recordings and editing. Quesada then decided he wanted to pursue his goal of becoming a producer and sound engineer.</p>
<br>
<p>He was still working hard as a producer and sound engineer, and also writing jingles and soundtracks for all forms of advertising and radio, until he was called up for an audition to be the bass player in Shaman.</p>
<br>
<p>Fernando was already a big fan of the band, and had been able to support them in many live shows in the past, and going to radio shows, interviews and all kinds of events. Joining the band as a member would be a dream come true for him. </p>
<br>
<p>He was supposed to write 3 songs for his audition, and it was more than enough to establish himself as the new bass player of Shaman. He is one of the few people in the world that can say he had a dream came true joining a band he was previously a huge fan.</p>
<br>
<p>So he was the bass player in Shaman, but besides that, he was still working on the studios and on the music production fiel, by signing productions of important names in the pop-rock music business, and even won awards for best production, mixing and writing. It was by then that he partnered with Thiago Bianchi and Gabriel Triani to open a new studio called Fusão VM&T.</p> 
<br>
<p>In late 2008, after the band arrived from the 'Immortal Tour' in Brazil and Latin America, Fernando released his first live DVD with Shaman, ANIME ALIVE. It was recorded live during the band's concert for more than 15 thousand people at the Anime event in Sao Paulo.</p>
<br>
<p>In addition, he was invited to be the audio coordinator and professor of the Advertising, Cinema, Radio and TV courses at CEUNSP University in Salto/SP. Quesada accepted the invitation and moved to Salto, reaching a new level in his professional career now, joining the faculty of a university.</p>
<br>
<p>In 2009 he went to the USA to produce and record a new singer, and had the opportunity to work with several big figures of great bands from NY, and even signed a partnership agreement with UNIVERSAL MASTERING STUDIOS NY, where all his production jobs are finished now.</p>
<br>
<p>In 2010, Quesada opened his own audio, video, music and design producer, HEADSTOCK. He also started teaching a music prodution course at EM&T, in Sao Paulo.</p>
<br>
<p>In 2011, Shaman launched Origins, an audio and a live DVD together. The DVD was recorded at the band's participation on the Masters Of Rock Festival in Prague, with the special participation of Bouslav Prague Orchestra. It was the first time in the history of the Brazilian rock scene that a Brazilian band joined an orchestra in Europe to record a DVD.</p>
<br>
<p>In 2013, Quesada continued with his activities and even made some trips to NY to record audios and videos. He also made lots of workshops and lectures on audio and the musical business. He's still teaching musical production courses at EM&T, where he is a teacher and a technical coordinator.</p>
<br>
<p>Plus, he has a new project in the way: a new band that's about to shake up the rock scene. Alongside their band mates in Shaman and great drummer Aquiles Priester on drums, here comes NOTURNALL!</p>	
