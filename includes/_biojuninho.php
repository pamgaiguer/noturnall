<p style="font-size: 20px">BIOGRAFIA</p><br>
					
<p>Juninho Carelli começou a estudar piano aos 9 anos de idade com o tecladista do conjunto Voz da Verdade Evaristo Fernandes. Estudou no conservatório de música Fundação das Artes de São Caetano do Sul, onde cursou piano e teoria musical. Após concluir seus estudos e já atuando como profissional no mercado, estudou na EM&T com Emílio Mendonça para aprimorar sua técnica e linguagem musical, além de cursos livres e workshops de musica em geral, voz e piano.</p>
<br>
<p>Passou por bandas de baile e shows, projetos autorais que foram desde musica instrumental e experimental a bandas de pop, rock e metal, compondo, produzindo, cantando e tocando. Aos 15 anos de idade já estava inserido no mundo profissional da música em palcos e gravações.</p>
<br>
<p>Já tocou e fez arranjos na Big Band Passaporte Brasil do maestro e amigo Carlos Ferreira, acompanhou a cantora Lydia Moises, tocou em orquestras de baile (junto com músicos conceituados que já acompanharam grandes nomes da música como Miles Davis), na banda de baile Koisa Nossa (onde abriu grandes shows para artistas como Lulu Santos, Zeca Pagodinho, Leonardo, Bruno e Marrone, Família Lima, Rádio Taxi...), gigs de jazz e musica instrumental, entre outros projetos e bandas que só o fizeram crescer musicalmente e profissionalmente como a amiga e cantora de musica pop e latina Kelly Moore!</p>
<br>
<p>Hoje, também graduado em Rádio, TV e publicidade, é tecladista/pianista da banda Noturnall, Shaman , acompanha a banda Brothers Music (projeto de Samuel Moises, guitarrista do conjunto Voz da Verdade), ex-tecladista na banda Viva Noite do programa Pânico na Band , lidera o Projeto "Juninho Carelli - A PONTE", acompanha o percussionista Guga Machado em seu projeto instrumental e produz música eletrônica em um projeto chamado House Rock Melon. Também gravando o disco solo instrumental do baterista Aquiles Priester .</p>
<br>
<p>É produtor musical e sócio diretor da empresa FOGGY Filmes que produz na área de comunicação, editando, roteirizando, produzindo e dirigindo projetos de áudio e vídeo, como clipes, documentários, curtas, conteúdo de entretenimento para tv e web. Foi diretor do programa Voz da Verdade exibido na RedeTV aos sábados, diretor de clipes internacional, professor universitário de edição de video e áudio, Professor de áudio e vídeo na EM&T Escola de Música e Tecnologia.</p>	
