							<p>NOTURNALL! This is the name of the new Brazilian metal supergroup formed by members of Shaman, Thiago Bianchi (vocals), Fernando Quesada (bass), Leo Mancini (guitar), Junior Carelli (keyboard) plus the renowned drummer Aquiles Priester (Hangar, ex-Angra).</p>

							<p>As expected, the band was already born big, and it's been considered the new Brazilian Rock/Metal sensation, not only because its members form a 'dream team' of music, but mainly for bringing an innovative musical approach: a modern, heavy and aggressive sound, yet melodic and progressive, as it was never heard before!</p>

							<p>Recorded at the Fusão VM&T Studios in São Paulo and at the Shanty Shack Studios in New Jersey/USA, Noturnall's self-titled debut counts with the lead singer of Symphony-X and Adrenaline Mob, Sir Russell Allen, as one of its producers. </p>

							<p>The album has already beaten some records since its release in February. It remained for five consecutive weeks in the first place among the best-selling titles of Die Hard Records - one of the top music stores in the country - after havin beaten the record of pre-orders in the same store. The album appears in second place as the best seeling record at the "Parede" section of Roadie Crew Magazine (issue #182 from March/2014), beating releases from bands like Epica and Within Temptation.</p>

							<p>The reception of Noturnall's debut album by the specialized press does not contradict the excellent sales figures. The well respected journalist Marcelo Moreira published on his blog "Combat Rock UOL" that "everything in the album is of good taste and very well done, with a flawless production and well structured arrangements". Ricardo Seelig, editor of the site "Collector's Room", declared that "the paradigm shift of Noturnall's debut album is what makes it amazing". Território da Musica web portal is one of the musical vehicles that did not spare praise to the album: <em>"interesting balance between technique, weight, melodies and arrangements... a great debut".</em></p>

							<p>Two music videos have already been released to promote the album. The first one for the song "Nocturnal Human Side", recorded entirely in New York and featuring a special cameo appearance by the band's producer, Russell Allen. Youtube view rank surpassed 150,000 in less than a month of the release. The second video, for "No Turn At All", one of the heavier songs in the album,  was filmed in the famous 'Team Nogueira' gym, with the "blessing" of their owners, the MMA fighter brothers, Minotauro and Minotouro.</p>

							<p>The debut of Noturnall on stage happened on March, 29th 2014, with a mega show at Carioca Club in São Paulo. Russell Allen was present, and cameo in a repertoire that included all the songs from the album, plus some nice surprises that amazed the fans.</p>

							<p>Completely sold out, the show was also a charity event, and raised more than 1ton of food plus many toys and diapers, which were donated to Casa Hope (www.casahope.org.br), an institution supporting children with cancer.</p>

							<p>The event was also opportunity to record the first DVD of the band. Tons of equipments were used to capture the whole show, including 15 cameras, 3 cranes and a large 32ft long led screen.</p>

							<p>Scheduled for release in July 2014, the DVD is being produced by three different companies: FX Render, owned by the award-winning director Alex Batista - who had already worked with the band in the music video for "Nocturnal Human Side"; Foggy Films, responsible for the second music video "No Turn At All"; and Fusão VM&T Studios.</p>

							<p>The event was also a perfect closing for the successful workshop tour Russell Allen and Thiago Bianchi performed in 15 Brazilian cities throughout the month of March of 2014.</p>

