							responsável pelo segundo videoclipe da banda, "No Turn At All" - além da Fusão
							Estúdios.</p>
							<p>O evento também marcou o encerramento da bem sucedida turnê de workshops
							realizada por Russell Allen e Thiago Bianchi que passou por 15 cidades brasileiras ao
							longo do mês de Março.</p>
							<p>O Noturnall segue agora pelas estradas de todo país promovendo seu já aclamado
							disco de estréia, também lançado pela Icarus em todo o restante da América Latina e
							pela Metal Ville nos Estados Unidos e Europa.</p>
