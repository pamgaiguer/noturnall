							produtores o vocalista do Symphony X e Adrenaline Mob, Russell Allen, e tem batido
							alguns recordes desde o seu lançamento em Fevereiro. O disco permaneceu por cinco
							semanas consecutivas em primeiro lugar entre os títulos mais vendidos da Die Hard
							Records - uma das principais lojas especializadas do país - além de ter batido o recorde
							de pré-vendas no mesmo estabelecimento. Na edição #182 (Março/2014) da revista
							Roadie Crew, o disco aparece na seção “Parade” no segundo lugar ente os mais
							vendidos, batendo lançamentos de bandas como Epica e Within Temptation.</p>
							<p>A recepção da imprensa especializada ao álbum de estréia do Noturnall não tem feito
							oposição aos excelentes números de vendas. O respeitado jornalista Marcelo Moreira
							publicou em seu blog Combate Rock do UOL que “tudo no álbum é de bom gosto e
							bem feito, com produção irretocável e arranjos bem estruturados”. Já Ricardo Seelig,
							editor do site Collector’s Room, declarou que “a quebra de paradigmas torna o primeiro
							álbum do Noturnall surpreendente”. O Portal Território da Música foi outro veículo que
