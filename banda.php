							um ótimo disco de estréia.”.</em></p>
							<p>Dois videoclipes já foram lançados para promover o álbum. O primeiro para a música
							“Nocturnal Human Side”, inteiramente gravado em Nova Iorque, e que também contou
							com a participação mais que especial do próprio produtor da banda, Russell Allen. As
							visualizações no Youtube ultrapassaram 150 mil em menos de um mês de seu
							lançamento.<br>
							Já o segundo vídeo foi para a música “No Turn At All”, uma das mais pesadas do
							álbum. O clipe foi filmado na famosa academia Team Nogueira, com a “benção” de
							seus proprietários, os irmãos lutadores de MMA, Minotauro e Minotouro.</p>
							<p>A estréia do Noturnall nos palcos aconteceu no dia 29 de Março com um super show
							histórico no Carioca Club em São Paulo. Na ocasião, o próprio Russell Allen fez uma
							participação especial num repertório que contou com todas as músicas do álbum de
							estréia além de muitas surpresas que emocionaram os fãs.</p>
							<p>Completamente sold-out , o evento beneficente arrecadou mais de 1 tonelada de
							alimentos, além de muitos brinquedos e fraldas que foram doados para a Casa Hope -
							<a href="http://www.casahope.org.br" target="_blank">www.casahope.org.br</a>, instituição de apoio a crianças com câncer.</p>
							<p>O show também serviu para a gravação do primeiro DVD do Noturnall. Foram
							disponibilizados várias toneladas em equipamentos, incluindo 15 câmeras, três gruas e
							um telão de led com mais de 10 metros de comprimento.</p>
							<p>Com previsão de lançamento para Julho de 2014, o DVD está sendo produzido por três
